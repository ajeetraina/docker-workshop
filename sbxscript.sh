#
# Applies the Docker Sandbox content drop to ajeetraina/docker-workshop:
#   - 11 new markdown files under docs/lab8/
#   - mkdocs.yml sidebar updates
#
# Run from inside a fresh checkout of docker-workshop:
#   git clone https://github.com/ajeetraina/docker-workshop.git
#   cd docker-workshop
#   bash /path/to/apply-docker-sandbox-content.sh
